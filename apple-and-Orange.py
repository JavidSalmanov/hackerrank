def countApplesAndOranges(s, t, a, b, apples, oranges):
    apples_count = 0
    orange_count = 0
    for apple in range(len(apples)):
        if(s <= apples[apple] + a <= t):
            apples_count += 1
    for orange in range(len(oranges)):
        if(s <= oranges[orange] + b <= t):
            orange_count += 1
    print(apples_count)
    print(orange_count)

